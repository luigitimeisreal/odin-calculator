function add(num1, num2) {
  return num1 + num2;
}

function subtract(num1, num2) {
  return num1 - num2;
}

function multiply(num1, num2) {
  return num1 * num2;
}

function divide(num1, num2) {
  return Math.floor(num1 / num2);
}


console.log(add(2, 1));
console.log(subtract(5, 1));
console.log(multiply(2, 2));
console.log(divide(10, 3));